import { describe, expect, it } from 'vitest';
import { existsSync, mkdirSync, readFileSync } from 'fs';
import { join } from 'path';
import { ClaudeCodeAdapter } from '../claude-code/index.js';
import { cleanupFixture, createProfileFixture, type Layout } from './helpers.js';

for (const layout of ['v1', 'v2'] as Layout[]) {
  describe(`ClaudeCodeAdapter (${layout})`, () => {
    it('exports CLAUDE.md, hooks, settings, and skills', async () => {
      const { rootDir, profilePath } = createProfileFixture(layout);
      const outDir = join(rootDir, 'export-claude');

      try {
        mkdirSync(outDir, { recursive: true });
        const adapter = new ClaudeCodeAdapter();
        await adapter.export(profilePath, outDir, {
          includeSkills: true,
          includePlaybooks: true,
        });

        expect(existsSync(join(outDir, 'CLAUDE.md'))).toBe(true);
        expect(existsSync(join(outDir, '.claude', 'settings.local.json'))).toBe(true);
        expect(existsSync(join(outDir, '.tastekit', 'hooks', 'session-orient.sh'))).toBe(true);
        expect(existsSync(join(outDir, 'skills', 'manifest.v1.yaml'))).toBe(true);

        const claudeMd = readFileSync(join(outDir, 'CLAUDE.md'), 'utf-8');
        expect(claudeMd).toContain('Generated by TasteKit');

        const settings = JSON.parse(readFileSync(join(outDir, '.claude', 'settings.local.json'), 'utf-8'));
        expect(settings.hooks).toBeDefined();
      } finally {
        cleanupFixture(rootDir);
      }
    });
  });
}
